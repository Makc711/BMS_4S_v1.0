void startADCConversion()
{
	HAL_ADCEx_Calibration_Start(&hadc);
	HAL_ADC_Start_DMA(&hadc, (uint32_t*)&adcData, NUMBER_OF_ADC_CHANNELS);
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc1)
{
	if (hadc1->Instance == ADC1)
	{
		HAL_ADC_Stop_DMA(hadc1);
		for (uint8_t i = 0; i < NUMBER_OF_ADC_CHANNELS; ++i)
		{
			adcSumData[i] += adcData[i];
		}
		counterOfADCMeasurements++;
		if (counterOfADCMeasurements == NUMBER_OF_ADC_MEASUREMENTS)
		{
			for (uint8_t i = 0; i < NUMBER_OF_ADC_CHANNELS; ++i)
			{
				adcResultData[i] = adcSumData[i] / NUMBER_OF_ADC_MEASUREMENTS;
				adcSumData[i] = 0;
			}
			counterOfADCMeasurements = 0;
		}
		startADCConversion();
	}
}

uint16_t getADCData(const ADCIndex index)
{
	return adcResultData[index];
}
